#python3基本数据类型
#python 中的变量不需要声明，每个变量在使用前都必须赋值，变量赋值以后该变量才会被创建
#在python中，变量就是变量，他没有类型，我们所说的“类型”是变量所指的内存中对象的类型
#等号（=）用来给变量赋值。

counter = 100#整型变量
miles = 1000.0#浮点型变量
name = "runoob"#字符串

print(counter)
print(miles)
print(name)

print('==========多个变量赋值==========')
a=b=c=1
print('a:',a,'b:',b,'c:',c)

d,e,f = 1,2.0,'runoob'
print('d:',d,'e:',e,'f:',f)
